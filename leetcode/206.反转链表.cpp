class Solution {
public:
    ListNode* reverseList(ListNode* head) {
        ListNode* prev = nullptr;
        ListNode* cur = head;
        while (cur) {
            ListNode* next = cur->next; // 先保存下一个节点
            cur->next = prev;           // 反转当前节点的指针
            prev = cur;                // 移动 prev 和 cur 指针
            cur = next;
        }
        return prev; // prev 是新的头节点
    }
};
